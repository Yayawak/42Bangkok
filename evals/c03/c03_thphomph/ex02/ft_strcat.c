char	*ft_strcat(char *dest, char *src)
{
	int	i;
	int	j;

	i = 0;
	while (dest[i] != '\0')
	{
		i++;
	}
	j = 0;
	while (src[j] != '\0')
	{
		dest[i] = src[j];
		i++;
		j++;
	}
	dest[i] = '\0';
	return (dest);
}
#include <unistd.h>
#include <stdio.h>
int main()
{
	char s1[100] = "asdfgh123456 ";
	char s2[] = "asdfgh456";
	ft_strcat(s1, s2);
	
	printf("%s", s1);
}
