int	ft_strcmp(char *s1, char *s2)
{
	int	i;
	int	cmp;

	i = 0;
	while (s1[i] || s2[i])
	{
		cmp = s1[i] - s2[i];
		if (cmp > 0)
			return (cmp);
		if (cmp < 0)
			return (cmp);
		i++;
	}
	return (0);
}
